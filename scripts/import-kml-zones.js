// Parse KML data and convert to zones
function parseKMLToZones(kmlData) {
  const zones = [];
  const placemarkRegex = /<Placemark>([\s\S]*?)<\/Placemark>/g;
  let match;
  let zoneId = 1;

  while ((match = placemarkRegex.exec(kmlData)) !== null) {
    const placemark = match[1];
    
    // Extract name
    const nameMatch = placemark.match(/<name>(.*?)<\/name>/);
    const name = nameMatch ? nameMatch[1].trim() : `Zone ${zoneId}`;
    
    // Extract description - use [\s\S] instead of . to match newlines, and add 's' flag for dotall
    const descMatch = placemark.match(/<description><!\[CDATA\[([\s\S]*?)\]\]><\/description>/);
    const description = descMatch ? descMatch[1].replace(/<br\s*\/?>/gi, '\n').trim() : undefined;
    
    // Check for MultiGeometry (multiple polygons)
    const hasMultiGeometry = placemark.includes('<MultiGeometry>');
    let allCoordinates = [];
    
    if (hasMultiGeometry) {
      // Extract all coordinate blocks from MultiGeometry
      const multiCoordRegex = /<coordinates>([\s\S]*?)<\/coordinates>/g;
      let coordMatch;
      while ((coordMatch = multiCoordRegex.exec(placemark)) !== null) {
        const coordString = coordMatch[1].trim();
        const coordLines = coordString.split(/\s+/).filter(line => line.trim());
        const coords = coordLines.map(line => {
          const parts = line.split(',');
          const lng = parseFloat(parts[0]);
          const lat = parseFloat(parts[1]);
          return { lat, lng };
        });
        allCoordinates = allCoordinates.concat(coords);
      }
    } else {
      // Single polygon
      const coordMatch = placemark.match(/<coordinates>([\s\S]*?)<\/coordinates>/);
      if (!coordMatch) continue;
      
      const coordString = coordMatch[1].trim();
      const coordLines = coordString.split(/\s+/).filter(line => line.trim());
      
      // Convert KML format (lng,lat,altitude) to app format (lat, lng)
      allCoordinates = coordLines.map(line => {
        const parts = line.split(',');
        const lng = parseFloat(parts[0]);
        const lat = parseFloat(parts[1]);
        return { lat, lng };
      });
    }

    if (allCoordinates.length < 3) continue; // Need at least 3 points for a polygon

    // Calculate bounds
    const lats = allCoordinates.map(c => c.lat);
    const lngs = allCoordinates.map(c => c.lng);
    const minLat = Math.min(...lats);
    const maxLat = Math.max(...lats);
    const minLng = Math.min(...lngs);
    const maxLng = Math.max(...lngs);
    const centerLat = (minLat + maxLat) / 2;
    const centerLng = (minLng + maxLng) / 2;

    const zone = {
      id: zoneId++,
      name,
      coordinates: allCoordinates,
      minLat,
      maxLat,
      minLng,
      maxLng,
      centerLat,
      centerLng,
      description,
      visible: true,
      locked: false,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    };

    zones.push(zone);
  }

  return zones;
}

// Main execution
const zones = parseKMLToZones(kmlData);
